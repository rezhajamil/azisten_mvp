     */
    public function store(StoreReviewRequest $request)
    {
        $request['service_id']=$request->service;
        $review=Review::create($request->all());

        $table=Service::select('initial')->where('id',$request->service)->first();
        
        DB::table($table->initial)->where('id',$request->id)->update(['review_id'=>$review->id]);

        return redirect('/');
    }

